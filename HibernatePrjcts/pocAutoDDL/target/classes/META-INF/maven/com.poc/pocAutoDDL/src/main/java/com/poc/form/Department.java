package com.poc.form;

public class Department {

	  private int deptID;
	  private String deptName;
	  private String deptHOD;
	  private String deptEvents;
	  
	  public int getDeptID()
	  {
	    return this.deptID;
	  }
	  
	  public void setDeptID(int deptID)
	  {
	    this.deptID = deptID;
	  }
	  
	  public String getDeptName()
	  {
	    return this.deptName;
	  }
	  
	  public void setDeptName(String deptName)
	  {
	    this.deptName = deptName;
	  }
	  
	  public String getDeptHOD()
	  {
	    return this.deptHOD;
	  }
	  
	  public void setDeptHOD(String deptHOD)
	  {
	    this.deptHOD = deptHOD;
	  }
	  
	  public String getDeptEvents()
	  {
	    return this.deptEvents;
	  }
	  
	  public void setDeptEvents(String deptEvents)
	  {
	    this.deptEvents = deptEvents;
	  }
}
